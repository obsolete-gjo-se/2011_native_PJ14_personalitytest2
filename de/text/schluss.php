Verbringen Sie mehr Zeit im Freien
In den 80er Jahren wurden als Hobby sportliche Aktivitäten an der
frischen Luft am häufigsten genannt. Im Jahr 2010 hieß das Lieblingshobby
der meisten 'Fernsehen'. Diese Aussage signalisiert eine übergreifende
Tendenz der westlichen Gesellschaft: Der Mensch verbringt
mehr und mehr Zeit in geschlossenen Räumen. Gegen das Fernsehen
ist zwar grundsätzlich nichts einzuwenden, aber extensiv als Hobby
betrieben ergibt sich folgendes Problem: Der menschliche Körper hat
Tausende von Jahren damit verbracht, sich an ein Leben in freier Natur
anzupassen. Ähnlich wie eine Pflanze, die Wasser und Licht zum
Überleben braucht, funktioniert unser Körper ohne Sonne und Sauerstoff
auch nicht besonders gut. Sobald Sonnenlicht auf unsere Augen
trifft, werden Hormone freigesetzt, die sich auf unser Nervensystem
und unsere Laune auswirken. Außerdem hängt die Produktion von
Vitamin D, das für das Mineralstoffgleichgewicht unseres Körpers
sorgt, von der Menge an Sonneneinstrahlung auf unsere Haut ab.
In den 90er Jahren fiel in Forscherkreisen häufiger der Begriff 'Sick
Building Syndrome', wörtlich übersetzt 'Krankes-Gebäude-Syndrom'.
Psychologen hatten festgestellt, dass es in manchen Gebäuden ein und
derselben Firma häufiger Krankheitsausfälle gab als in anderen. Bei
größeren Krankheitswellen wurden Teams von Medizinern damit beauftragt,
die Ursachen zu ergründen. Ohne Ergebnis. Erst nach einiger
Zeit stellten Wissenschaftler einen Zusammenhang zwischen den
Büroräumen selbst und einer erhöhten Krankheitsquote fest. In einem
Büro ohne Fenster verliert der Körper seine Orientierung. Er weiß
nicht mehr, wann er auf Tages- und wann er auf Nachtmodus schalten
soll. Das verursacht Stress für den Körper, und eines Tages bricht das
Immunsystem zusammen. Seit dieser Erkenntnis wird beim Bau von
Bürokomplexen verstärkt darauf geachtet, dass genügend Sonnenlicht
und Frischluft eingeplant werden. Eine Investition, die sich rechnet,
denn die Mitarbeiter arbeiten effektiver und werden seltener krank.
Öffnen Sie zu Hause die Gardinen, und verbringen Sie jeden Tag
etwas Zeit im Freien. Wenn Sie in einem Büro arbeiten, dass nur we-
55
nige Fenster hat, machen Sie täglich einen kleinen Mittagsspaziergang.
Außerdem sollten Sie mindestens eine (echte) Pflanze im Büro haben.
Österreichische Psychologen haben festgestellt, dass Pflanzen wesentlich
zum Abbau von Stresshormonen beitragen. Für ein glücklicheres
Leben brauchen wir Sonne und frische Luft. Ohne sie fühlt sich der
menschliche Körper nicht nur physisch, sondern auch psychisch unwohl.
Gönnen Sie Ihrem Gehirn regelmäßig eine Pause
Das Zeitalter der schnellen Veränderungen wirkt sich auch auf unsere
Aufnahmekapazität und unser Konzentrationsvermögen aus. Für unsere
Vorfahren steckte die Umgebung voller Gefahren. Um zu überleben,
lernten sie, besonders auf drei Dinge zu achten: 1. leuchtende
Farben (um Früchte schnell zu entdecken und giftige Nahrung von
genießbarer zu unterscheiden), 2. sich wiederholende Muster (um
Landschaften wiederzuerkennen und sich den Weg nach Hause einzuprägen)
und 3. Gesichtsausdrücke (um festzustellen, ob das Gegenüber
freundlich oder feindlich gesinnt ist).
Das Gehirn hat dieses Wahrnehmungsmuster über Jahrtausende
gelernt und trainiert - und so widmet es diesen drei Dingen noch heute
seine besondere Aufmerksamkeit. Etwas Leuchtendes, Blinkendes,
sich Wiederholendes wird von unserem Gehirn immer als Erstes
wahrgenommen. Das Problem ist nur: Heutzutage besteht unsere gesamte
Umgebung aus bunten Signalen. Der Alltag ist ein einziges
Feuerwerk aus grellem Neonlicht, blinkenden Leuchtreklamen und
knalligen Werbeplakaten - in einem sich ständig wiederholenden
Muster. Noch dazu begegnen wir auf der Straße und im Fernsehen
täglich Hunderten von Menschen mit den unterschiedlichsten Gesichtsausdrücken.
Es ist daher nicht verwunderlich, dass unsere Aufnahmefähigkeit
sinkt und es uns schwer fällt, uns auf ein bestimmtes Detail
zu konzentrieren. Unser Gehirn ist überfordert. In den USA werden
inzwischen zwei von fünf Kindern aufgrund schwerer Konzentrationsstörungen
medikamentös behandelt. Die Anzahl der Erwachsenen,
56
die ebenfalls unter diesen Störungen leiden, hat sich in den letzten
zehn Jahren verdoppelt. Immer häufiger wird medikamentös gegen
diese Symptome vorgegangen. Allerdings sprechen sich viele Experten
gegen eine Behandlung mit Medikamenten zur Lösung des Problems
aus und plädieren dafür, den Menschen statt Medikamenten eine gesündere
Umgebung zu verordnen.
Schalten Sie mindestens einmal in der Woche ab. Lassen Sie den
Fernseher aus und das Mobiltelefon zu Hause. Nehmen Sie sich Zeit
dafür, andere Menschen, Ihr Umfeld und vor allem sich selbst zu erfahren.
Ein ausgedehnter Spaziergang im Wald oder Park wäre ein
guter Anfang. Genießen Sie das satte Grün der Pflanzen, streichen Sie
über die raue Borke eines Baumes und lauschen Sie dem Vogelgezwitscher
- denn Leben heißt, bewusst zu erleben. Widmen sie den kleinen
Details mehr Aufmerksamkeit und genießen Sie das warme, wohlige
Gefühl, wenn ein Hund Sie schwanzwedelnd begrüßt oder ein Fremder
Sie einfach anlächelt. Für ein glücklicheres Leben braucht das Gehirn
regelmäßig eine kleine Entspannungspause. Denn wenn unser Gehirn
überfordert ist, nehmen wir nur noch die Dinge an der Oberfläche
wahr.
Lernen Sie sich selbst kennen
Die Liebe zu sich selbst ist der Grundstein für ein glückliches, zufriedenes
Leben. Das haben nicht nur Wissenschaftler, sondern schon
zahlreiche Philosophen der unterschiedlichsten Zeitalter festgestellt.
Aber wie können Sie jemanden lieben, den Sie gar nicht wirklich
kennen? Darum: Unternehmen Sie eine Entdeckungsreise in Ihr Inneres.
Bei genauer Betrachtung werden Sie vieles wiederentdecken, was
Sie bereits kannten, Sie werden aber auch neue Beobachtungen machen
und Ihr Bild von sich vervollständigen. Finden Sie heraus, was Sie
erreichen wollen, was Sie vom Leben erwarten - und was Sie wirklich
glücklich macht. Im heutigen Zeitalter der schnellen Veränderungen
lässt sich Erfolg nicht an materiellen Dingen festmachen - weder an
Besitz noch an Werten oder Eigenschaften, die andere definieren.
57
Machen Sie sich frei von materiellen Zwängen und von dem, was andere
von Ihnen erwarten.
Glück und Zufriedenheit kommen aus Ihrem Inneren.
Wir hoffen, dass Ihnen Ihr Ratgeber Spaß gemacht hat und dass er Sie
ein Stückchen auf Ihrem Weg zu einem erfüllten Leben begleiten wird.
Was Sie suchen und was Sie finden, liegt an Ihnen - nicht am Zufall,
nicht an anderen, nicht an Ihrer Umgebung oder an Ihrem Partner.
Wenn es darum geht, das Leben von Gregory zu gestalten, sind Sie
der Autor. Sie entscheiden, was passiert, denn an jedem Tag haben
Sie die Chance, ein neues, aufregendes Kapitel zu schreiben - in der
Geschichte Ihres Lebens.</p>

