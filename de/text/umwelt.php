Ihr familiäres Umfeld: Der "Geschwister-Effekt"
In vielerlei Hinsicht ist die Kindheit ein Langzeittraining für unser
späteres Leben. Die Ansichten unserer Eltern und Verwandten darüber,
wie sich Kinder verhalten sollten, schaffen die Voraussetzungen für
unseren zukünftigen Interaktions-Stil. Wenn ein Kind, das zu Wutausbrüchen
neigt oder sich allgemein widerspenstig verhält, dafür bestraft
wird, verändert sich der natürliche (biologische) Drang des Kindes,
auf diese Art zu reagieren. Ebenso kann es sich langfristig auf die
Gefühlsbetontheit eines Menschen auswirken, wenn die Eltern glauben,
es sei in Ordnung für Mädchen, aber nicht für Jungen, zu weinen und
Gefühle auszudrücken. Auch Ihr Grad an Kompromissbereitschaft
wurde stark von Ihren Kindheitserfahrungen beeinflusst. Ob Sie gelernt
haben, dass es gut ist, direkt zu sein und zu sagen, was Sie denken,
oder ob Ihre Eltern es wichtiger fanden, Rücksicht auf die Gefühle
anderer zu nehmen (sogar auf Kosten ihrer eigenen), beeinflusst die
Art, wie Sie im späteren Leben mit Menschen umgehen.
Neben dem Aufstellen von Regeln, was sie tun sollten und was
nicht, belohnen Eltern und Verwandte ihre Kinder auch auf subtile
Art für die Leistungen, die sie am meisten beeindrucken und die den
Familienbedürfnissen am ehesten entsprechen - und schaffen damit
ein wohlgerundetes "Familien-Team". Auch wenn Eltern es vielleicht
nicht erkennen oder beabsichtigen: sie setzen unterschiedliche Erwartungen
in jedes ihrer Kinder. Studien haben bewiesen, dass Eltern und
Verwandte in Familien mit mehreren Kindern eher die Unterschiede
