<p>Ihr Weg durch ein neues Zeitalter
Sie leben in einer aufregenden Zeit. Psychologen sprechen in diesem
Zusammenhang vom Zeitalter der Veränderungen. Heute erfahren wir
Dinge, die Menschen nie zuvor erfahren haben. Dabei beinflusst uns
heute am stärksten die Geschwindigkeit mit der die einzelnen Veränderungen
auf uns einströmen.
Das Zeitalter der schnellen Veränderungen
Vor Tausenden von Jahren bewegte sich der Mensch in einer völlig
anderen Umgebung als heute. Das Leben unserer Vorfahren spielte
sich hauptsächlich draußen in der freien Natur ab. Die Familie stand
im Mittelpunkt, und das Überleben hing von harter körperlicher Arbeit
ab. Und dies galt für Generationen. Die Enkel lebten genauso, wie
ihre Großeltern gelebt hatten. Entwicklungen wie etwa das Benutzen
von Werkzeug und dessen technische Verbesserungen vollzogen sich
langsam - so langsam, dass der Mensch genügend Zeit hatte, zu lernen,
damit umzugehen und sich an sie als Teil seines Lebens zu gewöhnen.
Heute leben wir in einer Zeit der ständigen Veränderungen - sie folgen
so rasch aufeinander und sind derart vielfältig, dass wir uns kaum an
eine Veränderung gewöhnen können, ehe die nächste Neuerung da
ist.
Aufgrund des rasanten technischen Fortschritts kann das Leben
heutzutage innerhalb nur eines Jahrzehnts oder weniger eine völlig
neue Form annehmen. Anfang der 90er Jahre hatte jeder durchschnittliche
Haushalt einen Fernseher, ein Telefon, aber noch keinerlei Erfahrungen
mit einem Computer. Wenn etwas nicht funktionierte, konnte
man es in der Regel selbst reparieren. Nur ein Jahrzehnt später gehörten
Computer schon fast zur Standardausstattung eines Haushalts. Heute
geht man davon aus, dass jedermann 24 Stunden lang über sein Handy
erreichbar ist, man verschickt E-Mails selbstverständlich rund um die
52
Welt und wundert sich, wenn jemand keinen Internet-Anschluss besitzt.
Aber seitdem computergesteuerte Geräte das Leben in allen Bereichen
erleichtern, ist es gleichzeitig für uns schwerer geworden, die Geräte
selbst zu reparieren. Für spezialisiertes Hightech benötigt man eben
Hightech-Spezialisten. Wir sind nicht mehr in der Lage, die Technik
und die Geräte, die uns umgeben, zu begreifen, geschweige denn,
Fehlfunktionen oder Schäden zu beheben. Wir sind nicht nur von den
Geräten abhängig, sondern auch von den Menschen, die uns mit den
Geräten helfen. Dieser Umstand repräsentiert nicht nur die Entwicklungen
der Technologie, sondern auch die damit verbundenen Veränderungen
in unserem täglichen Leben. Die neue Unabhängigkeit geht
Hand in Hand mit einer Beschränkung unserer Freiheit. Statistische
Erhebungen in Europa und Amerika belegen, dass die Menschen in
den letzten Jahrzehnten zunehmend unglücklicher geworden sind. Im
Jahr 2010 war die Unzufriedenheit mit dem eigenen Leben größer als
jemals zuvor.
Um dem Unglück und der Unzufriedenheit vorzubeugen, müssen
Sie gut gerüstet sein. Im Folgenden erfahren Sie, was Psychologen
für die drei wichtigsten Erkenntnisse dieses Jahrzehnts halten. Die
Basis aller drei Vorschläge zog sich als roter Faden durch diesen
Ratgeber: Um ein glückliches Leben führen zu können, müssen wir
uns bewusst werden, nach welchen Mustern wir agieren - in Bezug
auf unsere Interaktionen mit anderen, unsere Umgebung und unseren
Alltag.
Drei wichtige Erkenntnisse für ein glücklicheres Leben
Pflegen Sie persönliche Kontakte
Dank Mobiltelefon und Internet bleiben wir in Verbindung. Mit allen,
jederzeit und überall. Während unser bester Freund Urlaub macht,
können wir uns schnurlos nach den Highlights der Reise erkundigen.
Gleichzeitig mailen wir mal eben die neuesten Witze an unsere Cou-
53
sine, die mit ihrem Laptop in den USA unterwegs ist. Aber nicht die
Quantität, sondern vielmehr die Qualität unserer Interaktionen ist
entscheidend. Studien haben ergeben, dass wir seit Einführung modernster
Kommunikationstechnologien physisch immer weniger Zeit
mit anderen verbringen. Wir nehmen zwar häufiger Kontakt auf, unsere
Interaktionen bleiben aber oberflächlich. Wir schicken mal eben
eine SMS, anstatt uns persönlich zu erkundigen, wie es dem anderen
geht: wir surfen stundenlang im Internet und verabreden uns in virtuellen
Chat-Rooms, anstatt auszugehen, Freundschaften zu pflegen und
reale Menschen kennen zu lernen.
Hand in Hand mit der Entwicklung einer globalen Gesellschaft
entwickeln wir uns hin zu Individualisten. Fragen nach der Vereinbarkeit
von Beziehung und Unabhängigkeit, von Kindern und Karriere
belasten uns. Die Beliebtheit von Fernsehserien wie "Sex in the City"
spiegeln diese Tendenz zum Individualismus wider. Obwohl es so
aussieht, als hätten wir mehr Freunde als je zuvor, verbringen wir
mehr Zeit allein. Der technische Fortschritt und die Konzentration auf
uns selbst haben uns zwar mehr Freiheit und mehr Möglichkeiten gebracht,
aber auch weniger Tiefe und innige Verbundenheit in unseren
Beziehungen. Wir sind heute einsamer als jemals zuvor.
Nutzen Sie moderne Kommunikationstechnologien als zusätzliche
Möglichkeit zur Pflege Ihrer Kontakte, aber vernachlässigen Sie nicht
die persönlichen Begegnungen von Mensch zu Mensch. Im Hinblick
auf Klasse statt Masse werden Sie und auch Ihre guten Freunde davon
profitieren, wenn Sie einen Kern-Freundeskreis definieren und die
Kontakte intensiv pflegen, die Ihnen wirklich etwas bedeuten. Für ein
glücklicheres Leben brauchen wir das Gefühl der Verbundenheit, denn
dies gehört nun einmal zu unseren Grundbedürfnissen. Tiefe und innige
Beziehungen lassen sich aber nur auf einer 'persönlichen' Ebene aufbauen
und pflegen.
