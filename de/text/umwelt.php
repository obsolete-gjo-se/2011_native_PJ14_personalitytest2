<p>Wenn wesentliche Teile der Persönlichkeit auf biologischen oder genetischen
Faktoren beruhen, warum sind dann Brüder und Schwestern
(die doch dieselben Eltern haben und daher ähnliche Gene) oft so
verschieden? Diese Frage hat Psychologen jahrzehntelang beschäftigt
und ist eine gute Einleitung für diesen Abschnitt: eine Analyse der
erlernten oder ,gewohnheitsmäßigen' Seite Ihrer Persönlichkeit.
Wir alle kommen mit einem Nervensystem auf die Welt, das eine
bestimmte Funktionsweise vorzieht. Trotzdem ist unser Verhalten
nicht in Stein gemeißelt, denn unsere Persönlichkeit wird durch unsere
Erfahrungen geprägt und verändert. Viele verschiedene Einflüsse
wirken auf uns ein - von den Erwartungen unserer Eltern, Verwandten,
Lehrer, Kindheits- und Schulfreunde bis hin zu den Regeln unserer
Gemeinschaft und Gesellschaft, die alle einen bleibenden Eindruck
auf unserer Persönlichkeit und Art zu denken hinterlassen.
Während unserer gesamten Kindheit werden wir darin geschult,
uns so zu verhalten, wie andere es für angemessen halten, und so zu
denken, wie andere es nützlich finden. Dies veranschaulicht das folgende
Experiment: Welcher der vier Gegenstände passt Ihrer Meinung
nach nicht zu den anderen?
