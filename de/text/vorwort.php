<p>Was hinter uns liegt und was vor uns liegt, sind Kleinigkeiten im
Vergleich zu dem, was in uns liegt.<br />
Oliver Wendell Holmes</p>

<p>Lieber xyz,<br />
was macht uns glücklich? Wie leben wir ein zufriedenes Leben? Um
diese Fragen zu beantworten, führten wir eine Studie durch. Unser
Team von Wissenschaftlern verschickte Fragebögen an Seniorenheime,
in denen folgende Frage gestellt wurde: "Wenn Sie anderen Menschen
einen Rat geben könnten, wie man Glück und innere Zufriedenheit
erreicht, welcher wäre das?" Ziel der Umfrage war es, nicht nur die
häufigste Antwort zu erfahren, sondern auch Aufschluss darüber zu
erhalten, inwiefern Kindheit, Lebenserfahrungen und Beruf Einfluss
auf die Beantwortung dieser Frage haben. Gespannt erwarteten wir
eine große Bandbreite an Ratschlägen, was der Schlüssel zum Glück
sei. Doch sehr schnell stellten wir etwas Erstaunliches fest: Trotz ihrer
unterschiedlichen Herkunft und dem abweichenden beruflichen und
privaten Hintergrund der Absender kristallisierten sich zwei Kernaussagen
heraus. Danach ist die wichtigste Lektion, die das Leben in
punkto Glück und Zufriedenheit lehrt: <br />
1. Erkenne dich selbst, und <br />
2. Folge dem, was dein Herz dir rät.<br /><br />
Ein 91-jähriger Mann schrieb: "Es ist einfach, sein Leben auf andere
auszurichten. Jeder tut das. Ich denke aber, man muss sein Leben zuerst
für sich selbst leben." Eine 89-jährige Frau schlug vor: "Stellen Sie
sich Fragen. Denn wenn Sie Ihr eigenes Ich nicht verstehen - was Sie
mögen und warum das so ist - werden Sie auch nicht fähig sein, jemand
anderen wirklich zu verstehen." "Heutzutage kennen viel zu wenig
Menschen sich selbst. Sie haben keine Ahnung, was sie vom Leben
erwarten. Aber wenn sie nicht wissen, was sie wollen, wie sollen sie
es dann jemals erreichen?" "Begnügen Sie sich nicht damit, in Ihrem
eigenen Leben ein Beifahrer zu sein. Nehmen Sie das Steuer selbst in
die Hand. Lernen Sie, mit den Karten des Schicksals umzugehen, und
spielen Sie Ihr Blatt aus." Und zu guter Letzt: "Glück und Zufriedenheit
hängen davon ab, sich selbst zu erkennen und sich so zu akzeptieren,
wie man ist." "Um den Sinn des Lebens zu entdecken, müssen Sie in
Ihrem Inneren forschen. Schauen Sie in Ihr Herz und hören Sie auf
das, was es Ihnen sagt."</p>

		<p>Das Leben und das Glück sind veränderbar. Entscheidend sind
		die richtigen Veränderungen.<p>

<p>In diesem Ratgeber geht es um Sie. Sie können es als Ihren persönlichen
Leitfaden sehen - für den Umgang mit Ihrer Persönlichkeit, Ihrer
Form der Kommunikation und Ihrer Tendenz, sich auf eine bestimmte
Art zu verhalten.
Alle Erkenntnisse, die in Ihre Persönlichkeits-Auswertung eingeflossen
sind, stützen sich auf die Ergebnisse aus über 60 Jahren Persönlichkeitsforschung.
Nutzen Sie die Erkenntnisse von Psychologen
und Verhaltensforschern, um Ihren individuellen Weg zu Glück und
Zufriedenheit zu finden. Denn wenn Sie sich selbst verstehen, können
Sie Ihre Stärken nutzen, mit Ihren Schwächen besser umgehen und
so Ihr volles Potenzial ausschöpfen. Auf den folgenden Seiten werden
Sie lernen, zu erkennen, welche Menschen Ihnen gut tun und in welchen
Situationen Sie sich wohl fühlen. Sie werden auch erfahren, wie
zwischenmenschliche Beziehungen (Partnerschaften und Freundschaften)
entstehen und was diese Beziehungen für Sie ganz persönlich und
Ihr Leben bedeuten. Denn nicht nur das Verständnis der eigenen Persönlichkeit
und die individuellen Anforderungen an Freundschaften
und Partnerschaften, sondern auch die Erwartungen an die Liebe sind
essentielle Grundlagen für ein glückliches zufriedenes Leben. 'Liebe'
hat für jeden eine andere Bedeutung. Für jeden gleich sind jedoch die
Voraussetzungen, die zu einer glücklichen Liebesbeziehung führen.
Eine Studie, in der über 1.000 Paare (glückliche und auch unglückliche)
nach Ihrer Definition von Liebe gefragt wurden, ergab, dass man folgendes
genau kennen sollte: Die eigene Persönlichkeit, die eigenen
Erwartungen an eine Beziehung und die eigenen Bedürfnisse in einer
Partnerschaft. Dieser Ratgeber gibt Ihnen Möglichkeiten und Wege
für eine spannende Reise. Die Reise zu sich selbst.
Dabei wünschen wir Ihnen viel Spaß und Erfolg.</p>

