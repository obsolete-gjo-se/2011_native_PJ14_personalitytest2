<div>Denkmuster<br />
<p>Haben Sie sich schon einmal gefragt, warum Sie manche Dinge ganz
instinktiv tun? Warum Sie bestimmte Menschen, Aufgaben und Situationen
anderen vorziehen? Warum Sie überhaupt so fühlen, denken
und handeln, wie Sie es tun?
Die Antwort liegt in Ihrer Persönlichkeit. Sie ist es, die Ihre Interaktionen
mit dem Rest der Welt beeinflusst - immer und ohne Ausnahme.
Ihre Persönlichkeit ist das, was Sie von anderen Menschen unterscheidet.
Sie ist das Muster, nach dem Sie mit anderen kommunizieren,
nach dem Sie Aufgaben angehen und lösen, und sie bestimmt, ob Sie
offen oder verschlossen, ob Sie impulsiv oder überlegt, hart oder
weich, aktiv oder passiv sind. Ihre Persönlichkeit spiegelt sich nicht
nur in Ihren Einstellungen und Interessen wider, sondern auch darin,
wie Sie Informationen wahrnehmen, verarbeiten und speichern - wie
Sie Ihr Leben leben.</p>

<p>Was fällt Ihnen spontan zum Wort 'blau' ein?
Mancher nennt Begriffe aus der Natur (Himmel, Wasser oder Wal),
andere ein Gefühl oder einen Zustand (traurig, kalt, betrunken) und
einige überraschen mit vollkommen unerwarteten Assoziationen
(Tisch, Kuh). Wahrscheinlich denken Sie, dass die Antwort von der
jeweiligen Stimmung des Befragten abhängt und dass es sich bei solchen
Fragen nicht um einen ausgereiften Persönlichkeitstest handeln
kann. Sie haben Recht. Psychologische Forschungen haben jedoch
ergeben: Wenn man Ihnen in einigen Jahren noch einmal dieselbe
Frage stellt, werden Sie mit hoher Wahrscheinlichkeit exakt dieselbe
Antwort geben.</p>

<p>Dies könnte bedeuten, dass Sie ein hervorragendes Gedächtnis besitzen.
Wahrscheinlicher ist allerdings, dass diesem Phänomen Ihr
individuelles Denkmuster zugrunde liegt, das Sie von Kindheit an
entwickelt haben. Dieses Denkmuster können Sie sich wie Wanderwege
vorstellen, die durch Ihren Kopf führen - und Ihr Gehirn ist wie
eine einzigartige Landschaft mit Bergen, Tälern, Wäldern und Wiesen.
Je nachdem, welche Hindernisse auftauchen oder wie beschwerlich
der Pfad ist, gehen Sie einfachere Wege oder nehmen eine Abkürzung.
Mit der Zeit kennen Sie diese Wege immer besser, und später gehen
Sie automatisch diese Ihnen vertrauten Wege, um ans Ziel zu gelangen.
Die Menge an Informationen, die Sie speichern, verändert sich mit
den Jahren, doch die Art und Weise, wie Sie diese Informationen
sammeln, verwalten und nutzen, ebenso wie Ihre psychischen Reaktionen
auf diese, werden sich wahrscheinlich nicht verändern. Vielmehr
beginnt sich Ihre Persönlichkeit und Ihre Art zu denken bereits im
Alter von 13 Jahren herauszukristallisieren und ab 30 sind Ihre Persönlichkeit
und Ihre Denkmuster so weit gefestigt, dass sie sich bis zum
Ende Ihres Lebens nur wenig ändern werden. Das heißt, Sie werden
zwar Ihre Frisur ändern, neue Hobbys anfangen, Ihren Freundeskreis
wechseln oder eine andere politische Meinung vertreten, aber wer Sie
sind und wie Sie denken, bleibt im Wesentlichen gleich.
Bei der Interaktion mit der Welt bevorzugt Ihr Gehirn bekannte
Denkmuster.
Falls Sie einmal die Möglichkeit haben, am 25. oder sogar 50. Jubiläum
Ihrer Abschlussklasse teilzunehmen, sollten Sie diese Gelegenheit auf
jeden Fall wahrnehmen. Ihnen würde sonst eine sehr besondere Erfahrung
entgehen. Vor Ihnen wird etwa ein Mensch stehen, der behauptet,
Ihr alter Freund Michael zu sein, den Sie seit 25 oder 50 Jahren nicht
mehr gesehen haben. Michael kennen Sie noch als schlaksigen Typen
mit wildem Haar, der immer Turnschuhe trug. Doch Ihr Gegenüber
ist jetzt nicht mehr schlank, hat nur noch wenig Haare auf dem Kopf
und trägt eine Krawatte. Trotzdem werden Sie innerhalb von ein paar
Minuten das Gefühl haben, sich wieder mit dem 16-Jährigen von damals
zu unterhalten. Seine Stimme klingt noch genauso, er hat immer
noch einen Blick für Details, er hat immer noch diese Eigenart, sich
am Ellbogen zu kratzen, wenn er nachdenkt, und er besitzt nach wie
vor denselben guten Humor.
Natürlich verändern wir uns mit der Zeit. Aber das Wesentliche
unseres Charakters bleibt bestehen - ganz gleich, wohin uns das Leben
führt. Ebenso wie wir mit der Veranlagung zum Rechts- oder Linkshänder
geboren werden, gibt es Teile unserer Persönlichkeit, die uns
von Anfang an leiten und beeinflussen - ein Leben lang.</p>

<p>Es ist wichtig, die eigene Persönlichkeit und ihre Grenzen zu kennen
- schließlich nimmt sie Einfluss auf alle Bereiche unseres Lebens.
Sobald Sie verstanden haben, was Ihre Verhaltens- und Denkmuster
sind, warum Sie die Dinge tun, die Sie tun, und wissen, in welchen
Situationen Sie sich am wohlsten fühlen, können Sie anfangen, das
volle Potenzial Ihrer Persönlichkeit zu nutzen. Und mehr noch: Sie
werden immer besser erkennen können, welche Situationen, welche
Umgebung und welche Menschen Ihnen gut tun.
Persönlichkeitsforscher haben herausgefunden, dass die Gesundheit
leidet, wenn Menschen im Widerspruch zu Ihrer Persönlichkeit leben.
Mit der Zeit sinken nicht nur Energie und Motivation, sondern auch
das Immunsystem reagiert geschwächt. Wenn beispielsweise ein extrovertierter
Mensch gezwungen ist, über einen längeren Zeitraum allein
und ohne Kontakt zu anderen zu arbeiten, wird nicht nur seine
Produktivität im Beruf, sondern auch seine Lebensfreude deutlich
abnehmen. Das Gleiche gilt für einen introvertierten Menschen, der
eine Gruppe von Mitarbeitern führen und häufig öffentlich auftreten
muss. Ohne die Möglichkeit, sich zurückzuziehen, wird er zunehmend
schlechter funktionieren und immer unzufriedener werden. Situationen,
in denen wir eine Rolle spielen müssen, die nicht zu unserer Persönlichkeit
passt, äußern sich in Müdigkeit, mangelndem Konzentrationsvermögen
sowie einer negativen Grundstimmung, die sich auf alle
Lebensbereiche auswirkt. Aus diesem Grund ist es wichtig zu wissen,
was gut für uns ist, damit wir Möglichkeiten erkennen und Chancen
nutzen können, die unseren persönlichen Stärken und individuellen
Denkmustern entgegenkommen.
Auf den folgenden Seiten finden Sie Ihre Persönlichkeits-Übersicht
und daran anschließend Ihre detaillierte Auswertung zu den fünf leitenden
Persönlichkeitsfaktoren. Diese Komponenten Ihrer Persönlichkeit sind Ihre so
genannten 'Organisations-Prinzipien' - sie wirken sich auf Ihre Beziehungen,
Ihre Arbeit, Ihren Umgang mit Stress, Ihre Gesundheit und
Ihren Lebensstil aus. Wenn Sie den Auswertungen, in denen es um
Ihre Stärken und Ihre Herausforderungen geht, besondere Aufmerksamkeit
widmen, werden Sie sicherlich eine Bestätigung dessen erfahren,
was Sie schon über sich wissen. Sie werden aber auch Neues über
sich erfahren und Ihren eigenen Blickwinkel schärfen und erweitern.
Nutzen Sie das volle Potenzial Ihrer Persönlichkeit und Sie werden
erstaunt sein, wozu Sie fähig sind.</p></div>

