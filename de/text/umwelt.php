Die Bedeutung der Geburtenfolge zu verstehen, kann Ihnen daher
wertvolle Einsichten in Ihre eigenen Verhaltensmuster und die anderer
vermitteln. Der folgende Abschnitt ist eine Zusammenfassung zur
Geburtenfolge und ihrem typischen Einfluss auf die Persönlichkeit.
Wenn Sie die Beschreibungen lesen, denken Sie auch einmal an Ihre
Freunde: Stimmen Sie mit dem Schema überein, das die Psychologen
voraussagen? Am Ende des Abschnitts finden Sie eine individuelle
Analyse, die Ihre Persönlichkeit mit Ihrer eigenen Position in der Familie
vergleicht. Scheidung, zusammengeführte Familien, die Geburtenfolge
Ihrer Eltern und viele weitere Komponenten können zu einer
veränderten Bedeutung der Geburtenfolge führen. Daher ist es möglich,
dass Sie dort, wo solche Einflüsse besonders stark waren, von den
folgenden Beschreibungen abweichen.
Die Auswirkungen der Geburtenfolge
Ältestes Kind - Der Einflussnehmer
Älteste Kinder haben ein Merkmal, das sie von ihren Geschwistern
unterscheidet - sie waren die Ersten, die sprechen, laufen und ihre
Spielzeuge benutzen lernten und die Ersten, die zur Schule gingen.
Dies gibt ihnen einen Vorsprung gegenüber ihren jüngeren Brüdern
und Schwestern und lässt sie ein Macht- und Kontrollbedürfnis entwickeln
- zugleich setzt es sie aber auch unter den subtilen elterlichen
Druck, ein gutes Beispiel zu geben. Das Ergebnis: Erstgeborene sind
hochgradig leistungsmotiviert und können stur auf ihrem Willen beharren,
um die Kontrolle zu wahren. Von den ersten 23 Astronauten,
die ins All geschickt wurden, waren 21 Erstgeborene oder Einzelkinder.
Hauptproblem: Älteste Kinder wollen einen Eindruck in der Welt
hinterlassen.
Wenn Sie mit einem Erstgeborenen im Streit liegen, wird er oder
sie nicht ohne Weiteres nachgeben, recht konkurrenzbewusst sein und
kein ,Nein' als Antwort akzeptieren. Erstgeborene setzen sich selbst
48
sehr unter Druck und erwarten ebenso viel von anderen. Positive Seite:
Erstgeborene sind ehrgeizig, fürsorglich (aufgrund ihrer frühen Betreuer-
Rolle) und leidenschaftlich. Negative Seite: Sie können gelegentlich
den Besserwisser spielen.
Mittleres Kind - Der Friedensstifter
Mittlere Kinder haben eine problematische Position in der Familie:
Sie überbrücken die Kluft zwischen dem ältesten Kind, das scheinbar
alle Rechte und Privilegien genießt, und dem jüngsten, das alle Aufmerksamkeit
zu bekommen scheint. Zwischen diesen zwei Extremen
gefangen, entwickeln mittlere Kinder ein gutes Verhandlungs- und
Kooperationsgeschick. Sie sind stille Beobachter, mit denen man leicht
auskommen kann, aber tief im Innern spüren sie das brennende Bedürfnis,
einen ganz speziellen Platz in der Familie zu haben. Sie wirken
normalerweise eher unauffällig, aber sobald Sie sie besser kennen
lernen, werden Sie verborgene, überraschende Seiten an ihnen entdecken
- sie sind wie ein untätiger Vulkan, dessen Feuer unter der
Oberfläche brodelt. Hauptproblem: Mittlere Kinder sind innerlich
zerrissen zwischen dem Wunsch, ,im Schatten zu bleiben', und dem,
,im Rampenlicht zu stehen'.
Mit Mittelgeborenen werden Sie kaum viele Auseinandersetzungen
haben, denn sie scheuen die Konfrontation. Sie sind flexible Friedensstifter,
die das Leben nehmen, wie es eben ist. Sie vergeben schnell,
aber vergessen niemals. Positive Seite: Sie haben hervorragende
Menschenkenntnis.
Jüngstes Kind - Der Spaßmacher
Jüngste Kinder haben eine klar definierte Rolle in der Familie: Sie
sind die Sonnenscheine und Aufheiterer. Daran gewöhnt, von Menschen
umgeben zu sein und viel Aufmerksamkeit zu bekommen, entwickeln
sie große Beziehungsfähigkeit und Menschenkenntnis. Die
49
spezielle Herausforderung jüngster Kinder ist, dass sie oft kein Vertrauen
in ihre eigenen Fähigkeiten haben, auch wenn sie hoch begabt
sind. Das rührt daher, dass sie - ungeachtet ihres Alters - immer als
das ,Baby' der Familie angesehen werden. Sie sind lässig, unkompliziert
und lieben es, Spaß zu haben, aber es kann ihnen an Ehrgeiz oder
an Leidenschaft für ein spezielles Interessengebiet mangeln. Hauptproblem:
Jüngste Kinder wollen sich beweisen (wissen aber nicht so recht,
wie).
Sie bevorzugen Berufe, in denen sie mit Menschen zu tun haben.
Ein jüngstes Kind erkennen Sie daran, dass er oder sie immer mitten
im Trubel zu finden ist. Positive Seite: Jüngste Kinder sind charmant,
gesellig und gelassen. Negative Seite: Sie sind etwas unberechenbar
- zuvorkommend in der einen Minute, schwierig in der nächsten.
Einzelkind - Der Kontroll-Mensch
Einzelkinder ähneln in vielerlei Hinsicht den Ältesten, wenn es um
Familienrollen geht. Bereits in frühem Alter lasten die Hoffnungen
und Träume ihrer Eltern auf ihren Schultern. Da sie mehr Übung im
Umgang mit Erwachsenen als mit gleichaltrigen Kindern haben, neigen
Einzelkinder zu Willensstärke und Entschlossenheit, kümmern sich
aber nicht sonderlich um die Aufrechterhaltung der Harmonie.
Hauptproblem: Einzelkinder wollen von niemandem abhängig sein.
Positive Seite: Einzelkinder sind leidenschaftlich und entschlossen.
Sie sind außerordentlich selbstgenügsam. Man erkennt sie an ihrer
Furchtlosigkeit und ihrer Selbstständigkeit. Negative Seite: Sie sind
es gewohnt, ihren Willen durchzusetzen, und es fällt ihnen schwer,
Kompromisse zu schließen.
Ihre Persönlichkeit und Ihre Position in der Familie
Wurde Ihre Persönlichkeit von Ihrer Position in der Familie beeinflusst?
