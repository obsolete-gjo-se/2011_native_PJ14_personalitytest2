<p>Bis jetzt haben Sie erfahren, wie die wichtigsten Bereiche Ihrer Persönlichkeit
ausgeprägt sind. Sie haben eine Vorstellung davon, wo
Ihre Stärken liegen und wo noch Herausforderungen auf Sie warten.
Im Folgenden erhalten Sie einen Überblick, wie Sie die Ausprägung
Ihrer Persönlichkeit im Vergleich zum allgemeinen Durchschnitt einschätzen
können. Dies gibt Ihnen Aufschluss darüber, welche Teile
Ihrer Persönlichkeit im Umgang mit anderen Menschen besonders
zum Tragen kommen.</p>

<p>Persönlichkeit ist relativ<br />
Beim Lesen Ihrer Persönlichkeits-Auswertung sollten Sie immer im
Hinterkopf behalten, dass die Maßstäbe, nach denen Wissenschaftler
Persönlichkeit messen, relativ sind. Nehmen wir zum Beispiel die
Aussage: "Sarah B. ist ein gesprächiger Mensch." Was heißt das eigentlich?
Im Vergleich zu einem Goldfisch plaudert jeder wie ein
Wasserfall. Neben einem Klatsch-Kolumnisten hingegen wirkt beinahe
jeder stumm wie ein Fisch. Man muss also fragen: "Im Vergleich zu
wem ist Sarah B. gesprächig?" Und die entsprechende Antwort wäre:
Im Vergleich zum Durchschnitt der Bevölkerung.
Wissenschaftler des 'Institute for Personality Assessment' (Institut
für Persönlichkeitsforschung) in Kalifornien haben interessante Entdeckungen
in Bezug auf die Art und Weise gemacht, wie sich unsere
Persönlichkeit im Vergleich zum Durchschnitt verhält. Sie haben die
Persönlichkeitsmerkmale und -entwicklungen einiger hundert Personen
vom Kindergarten bis zum Erwachsenenalter sehr genau untersucht.
Dabei haben Sie festgestellt, dass sich die meisten Merkmale mit zunehmendem
Alter in vorhersehbarem Maße geändert haben. Beispielsweise
nahm die Aktivität langsam ab und außerdem neigten die Personen
dazu, mit zunehmendem Alter weniger emotional und extrovertiert,
dafür aber wesentlich gründlicher zu werden. Bemerkenswert war
aber, dass diejenigen, die bereits in ihrer Jugend aktiver waren als
andere Gleichaltrige, auch im Alter noch immer aktiver waren. Das
heißt: Obwohl Sie mit 70 weniger aktiv sein werden als mit 20, sind
Sie wahrscheinlich vergleichsweise immer noch agiler als andere 70-
Jährige, wenn Sie schon in Ihrer Jugend über dem Durchschnitt lagen.
Persönlichkeitsstudien zeigen, dass beispielsweise 3-Jährige, die mehr
Angst vor unbekannten Dingen (in diesem Versuch ein Spielzeug-
Roboter) hatten als andere Kinder ihres Alters, auch noch mit 18 wesentlich
vorsichtiger waren als der Durchschnitt. Demnach verlieren
Kinder mit der Zeit zwar ihre Ängste, die Grundtendenzen ihres
Charakters - beispielsweise relativ ängstlich zu sein - bleiben jedoch
ein Leben lang bestehen.
Der Vergleich mit dem Durchschnitt bringt die Besonderheiten
Ihrer Persönlichkeit zum Vorschein.
Ein Persönlichkeitstest kann den Vergleich zum Durchschnitt herstellen
und damit eine relative Einschätzung einer Persönlichkeit bieten. Er
kann allerdings nicht vorhersagen, wie wir in jeder einzelnen Situation
reagieren werden. Schließlich sind wir an manchen Tagen aktiver als
an anderen und in manchen Stimmungen vielleicht entschlossener und
mutiger als sonst. Aber ein Persönlichkeitstest kann mit relativ hoher
Wahrscheinlichkeit sagen, wie wir uns an einem 'Durchschnitts-Tag'
im Verhältnis zum Durchschnitt der Bevölkerung verhalten werden.
Auf den folgenden Seiten finden Sie eine Grafik, die die Ausprägung
Ihrer acht Persönlichkeitsmerkmale im Vergleich zum Durchschnittsmann
oder der Durchschnittsfrau darstellt. Um Ihre Auswertung besser
einschätzen zu können, denken Sie bitte daran, dass 'Durchschnitt' ein
vollkommen wertfreier Maßstab ist, der weder 'gut' noch 'schlecht'
beinhaltet - und nicht einmal 'normal' bedeutet - sondern lediglich
besagt, dass diese Punktzahl 'am häufigsten' vorkommt. Die Bereiche,
in denen Sie am stärksten vom Durchschnitt abweichen, sind diejeni-
gen, die bei Ihnen am meisten hervorstechen, wenn Sie mit anderen
Menschen interagieren. Sie sind die Leitmotive Ihrer Persönlichkeit.
</p>
