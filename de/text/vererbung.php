<p>Manchmal sind wir in der glücklichen Lage, dass unsere Lebensumstände
ideal zu der Rolle passen, die wir unserer Persönlichkeit nach
spielen sollten. Es gibt aber auch genügend Situationen, in denen wir
uns überhaupt nicht wohl fühlen und uns fragen, wie es dazu kommen
konnte. Schade ist, dass wir diese Fragen häufig erst im Nachhinein
stellen, statt von vornherein darauf zu achten, dass wir uns aktiv Lebensumstände
suchen, die gut und richtig für uns sind. Eine weitere
Frage, die wir noch seltener stellen, ist "Könnte es an mir liegen, dass
ich so unzufrieden bin?" und "Woran liegt es, dass ich so geworden
bin?".</p>

<p>Unser Verhalten wird durch instinktgesteuerte Reaktionen beeinflusst.
Wir kommen bereits mit der Anlage, uns auf die eine oder andere Art
zu verhalten, auf die Welt, denn ein großer Teil unserer Persönlichkeit
ist genetisch determiniert und instinktgesteuert. Manche Babys sind
eher ruhig und ausgeglichen, andere angespannt und leicht erregbar.
Diese biologischen Veranlagungen leiten uns ein Leben lang. Sie
weisen uns automatisch die Richtung, wenn wir uns entspannen und
treiben lassen - und beeinflussen uns auch dann noch, wenn wir einen
komplett anderen Weg einschlagen möchten. Sicher ist Ihnen schon
einmal aufgefallen, dass sich manche Menschen in einer Extremsituation
ruhiger verhalten als andere. Diese Tatsache lässt sich auf die instinktgesteuerte
Programmierung des Nervensystems zurückführen.
In einer Stress-Situation sendet das Gehirn Signale an das Nervensystem,
dieses sagt der Leber, dass sie Zucker ausschütten soll, damit
dem Körper Energie zugeführt wird, was eine Verlangsamung des
Verdauungssystems, die Erweiterung der Pupillen und eine erhöhte
Adrenalinausschüttung zur Folge hat. Das Nervensystem mancher
Menschen ist sensibler als das anderer. Das heißt, die beschriebene
'Kettenreaktion' läuft schneller und heftiger ab, der Hormonschub ist
höher und hält länger an - und der sensible Mensch ist folglich nervöser
und aufgeregter.</p>
<p>Ein plakatives Beispiel für den von biologischen Veranlagungen
bestimmten Teil unserer Persönlichkeit ist der Vergleich zwischen
den Reaktionen von extrovertierten und introvertierten Menschen.
Wissenschaftler haben herausgefunden, dass sich die menschlichen
Gehirne darin unterscheiden, inwiefern ihre Tätigkeit aktiviert oder
verlangsamt wird. Extrovertierte Menschen haben eine andere Gehirnstruktur
als introvertierte Menschen. Dies verdeutlicht die Art und
Weise, wie beide Personengruppen auf ein traumatisches Erlebnis -
zum Beispiel einen Autounfall - reagieren. Das Gehirn eines Extrovertierten
'macht dicht', es wird sozusagen stumpf gegenüber den Geschehnissen
und wird sich später nur noch an Weniges erinnern. Durch
diesen Schutzmechanismus nimmt ein extrovertierter Mensch das
komplette Ausmaß des Unfalls mental gar nicht wahr und setzt sich
möglicherweise bereits am nächsten Tag wieder hinters Steuer. Ein
introvertierter Mensch reagiert anders. Wenn er in eine traumatische
Situation gerät, reagiert sein Gehirn wesentlich langsamer - der
Schutzmechanismus des 'Dichtmachens' setzt erst viel später ein. So
erlebt eine introvertierte Person den gleichen Autounfall mit der ganzen
Wucht 'des geistigen Aufpralls'. Während des Geschehens ist das Gehirn
des Introvertierten wach und aktiv - es nimmt jedes einzelne noch
so kleine Detail wahr. Diese Person kann sogar so stark alarmiert sein,
dass sie den kompletten Unfall-Verlauf wie in einer Zeitlupe aufnimmt.
Sowohl die Reaktion und Verarbeitung der introvertierten als auch
die der extrovertierten Person hat ihre Vor- und Nachteile. Introvertierte
werden sich an mehr erinnern können und den gleichen Fehler
wahrscheinlich kein zweites Mal begehen, allerdings wird sie der
Unfall psychisch stärker belasten als einen Extrovertierten.</p>

<p>Auch bei dem folgenden Beispiel kommen die instinktgesteuerten
Unterschiede zwischen der Persönlichkeit eines aufgeschlossenen
Extrovertierten und der eines eher schüchternen Introvertierten zum
Ausdruck. Stellen Sie sich vor, beide seien auf einer Party. Mit der
Zeit wird der Alkoholpegel immer höher und die Stimmung immer
ausgelassener. Beide fangen an, peinliche Bemerkungen zu machen
und sich ausfallend zu benehmen. Wie werden sie wohl reagieren,
wenn man sie am nächsten Tag auf ihr Verhalten anspricht? Der extrovertierte
Mensch wird wahrscheinlich amüsiert lachen und gleich
darauf fragen, wo die nächste Party stattfindet. Der introvertierte
Mensch - der sich noch peinlich genau an alles erinnert - wird sich
möglicherweise schämen und fragen, ob er jemals wieder eingeladen
wird. Aber noch etwas lässt sich anhand der Party-Situation gut verdeutlichen:
Der Ursprung der Verhaltensweisen, die wir entweder als
Schüchternheit oder als Aufgeschlossenheit bezeichnen. Sobald ein
introvertierter Mensch auf eine Party kommt, läuft sein Gehirn instinktiv
auf Hochtouren: Er registriert sofort, wer wo steht und sich mit
wem unterhält, beobachtet, wer wie gekleidet ist, und nimmt sogar
Mimik und Gestik der Gäste bei ihren Interaktionen wahr. Das heißt,
in einem Umfeld mit vielen Menschen hat das Gehirn eines Introvertierten
viel zu tun und ist in solchen Umgebungen leicht überstimuliert.
Aufgrund dieser Überstimulation hat der Introvertierte keine 'Kapazitäten'
frei, um sich selbst aktiv am Partytreiben zu beteiligen, und
empfindet die Situation als anstrengend. Deshalb ist eine introvertierte
Person nicht sehr stark an Partys oder Situationen, in denen viele
Menschen zusammentreffen, interessiert. Sie wird als schüchtern beschrieben
und als Mensch, der sich nicht besonders für andere interessiert.
Dabei ist es lediglich die biologische Disposition - nämlich wann
und wie stark das Gehirn aktiv wird - die einen introvertierten Menschen
dazu veranlasst, sich zurückzuziehen, um sich zu erholen und
neue Energie zu tanken. Das Gehirn einer extrovertierten Persönlichkeit
funktioniert da ganz anders: Es benötigt jede Menge Input, um auf
Touren zu kommen, da es weniger sensibel und etwas träger reagiert.
Der Extrovertierte umgibt sich gern mit vielen Menschen, braucht
Bewegung und Veränderung im Leben und ist daher auch offen für
die unterschiedlichsten Aktivitäten - denn er generiert seine Energie
von außen.
Die Rolle, die wir in der Gesellschaft einnehmen, wird mitbestimmt
von unserer biologischen Veranlagung.
Wenn wir davon ausgehen müssen, dass unser Verhalten stark von
Instinkten und biologischen Dispositionen beeinflusst wird, sollten
wir uns die Muster unserer natürlichen Reaktionen genauer ansehen.
Wir werden darin einige Erklärungen finden, warum wir so fühlen
und handeln, wie wir es tun. Stressmanagement-Kurse, Seminare zu
nonverbaler Kommunikation und Selbsthilfegruppen basieren auch
auf diesem Prinzip: Es gilt zu erkennen, wann der Körper auf welche
Art und Weise reagiert, um zu verstehen, wie die eigene Persönlichkeit
funktioniert und wie man bestimmte Verhaltensweisen, die einen
stören, ändern kann. Durch spezielle Übungen und indem Sie sich
immer wieder den Ihnen schwierig erscheinenden Situationen stellen,
werden Sie mit der Zeit in der Lage sein, Ihre eigenen Reaktionsmuster
zu erkennen und zu durchbrechen. Sie haben es selbst in der Hand.
Instinktgesteuerte Reaktionen lassen sich durch Training verstärken
oder reduzieren. Die folgenden Fragen können Ihnen dabei helfen,
Ihre persönlichen Muster zu erkennen:</p>
<p>Ihre vier Fragen<br />
Können Sie Ihre Gefühle wirklich so gut verbergen, wie Sie
glauben?<br />
Stellen Sie sich vor einen Spiegel. Denken Sie nun nacheinander an
einen glücklichen, einen lustigen, einen traurigen, einen ärgerlichen
und einen peinlichen Moment. Während Sie sich diese Situationen
vorstellen, sollten Sie einmal genau beobachten, wie bestimmte Gesichtspartien
und einzelne Muskeln reagieren. Mussten Sie schmunzeln?
Wurden Ihre Gesichtszüge härter? Sind Sie rot geworden? Zeigen
sich Ihre Gefühle stärker auf der rechten oder der linken Seite Ihres
Gesichts? Wahrscheinlich verrät Ihre linke Gesichtshälfte mehr über
Ihre Emotionen als die rechte und die rechte hat eine stärker ausgeprägte
Muskelstruktur. Beobachten Sie sich selbst in nächster Zeit
einfach einmal etwas genauer - wie ist Ihre Mimik und Gestik und
was verrät Ihre Körpersprache über Ihren Gemütszustand?
Zu welcher Tageszeit können Sie am besten arbeiten?
Wann sind Sie besonders fit und leistungsfähig? Und wann fühlen Sie
sich eher matt und müde? Wenn Sie einmal darauf achten, werden Sie
feststellen, dass es in Ihrer Energiekurve ein Muster gibt. Dieses wellenförmige
Auf und Ab spiegelt Ihren Metabolismus, Ihren so genannten
Biorhythmus wider. Über den Zeitraum eines Monats und unter
Einfluss der jeweiligen Jahreszeit mag sich die Wellenlinie zwar verschieben,
aber eine grundsätzliche Tendenz werden Sie erkennen.
Diese Erkenntnis können Sie nutzen - etwa beruflich bei der Entscheidung,
zu welcher Uhrzeit Sie wichtige Termine machen, wann Sie am
besten einen entscheidenden Job bearbeiten und wann Sie eine kleine
Pause einlegen.
Wovon lassen Sie sich ablenken?
Achten Sie einmal bewusst darauf, was Sie im Verlauf des Tages ablenkt
oder stört. Welche Situationen und welche Einflüsse Ihrer Umgebung
behindern Ihre Konzentrationsfähigkeit? Sind es vielleicht die
schlechten Lichtverhältnisse an Ihrem Arbeitsplatz, der Mangel an
frischer Luft oder ein permanentes Hintergrundgeräusch? Je nach Ihrer
Persönlichkeitsstruktur neigen Sie dazu, sensibler als andere auf äußere
Stimulierungen zu reagieren. Allgemein lässt sich aber auf jeden Fall
sagen, dass übermäßiges Fernsehen Ihre Reaktionszeit verlangsamt
und das Interesse an anderen Aktivitäten reduziert.
Was macht Sie nervös?
Auch wenn Sie kein besonders nervöser Mensch sind, kann es für Sie
interessant sein, einmal darauf zu achten, welche Situationen Sie unsicher
oder fahrig machen. Ein untrüglicher Indikator für Nervosität
sind beispielsweise Ihre Hände. Wissen Sie manchmal nicht, wohin
mit ihnen? Stecken Sie Ihre Hände dann in die Hosentaschen? Oder
spielen Sie an Ihrer Uhr? Oder greifen Sie nach dem symbolischen
Strohhalm und halten sich an irgendeinem Gegenstand fest? Das, was
man mit seinen Händen tut, geschieht instinktiv und unbewusst. Beobachten
Sie auch einmal, was andere mit ihren Händen tun. Vielleicht
kommt Ihnen einiges bekannt vor und Sie erkennen auch bei sich
Muster, die sich wiederholen.</p>

<p>Extrovertiertheit
Ein Elternteil von Ihnen ist wahrscheinlich introvertiert und nachdenklich.
Studien haben ergeben, dass Extrovertiertheit oder Introvertiertheit
zu 53% erblich bedingt wird. Dafür verantwortlich sind Unterschiede
in der Sensibilität einer Gehirnpartie, die sich Amygdala nennt.
Emotionalität
Es ist gut möglich, dass Ihre Mutter - ebenso wie Sie - recht impulsiv
und intensiv auf positive und negative Gefühle reagiert.
Studien an Zwillingen haben ergeben, dass Emotionalität zu 43%
erblich bedingt wird. Außerdem wurde festgestellt, dass der Grad der
Emotionalität von Kindern meistens dem der Mutter ähnlich ist.
Fokussiertheit
Ihr Vater ist wahrscheinlich ebenso gründlich, wie Sie es sind. Konzentriert
er sich erst auf die eine Aufgabe, bevor er die nächste angeht?
Studien an Zwillingen, die bei der Geburt getrennt wurden, haben ergeben,
dass Fokussiertheit zu 44% erblich bedingt wird. Außerdem
hat man festgestellt, dass der Grad der Fokussiertheit von Kindern
meistens dem des Vaters ähnlich ist.
Aufgeschlossenheit
Es besteht eine große Wahrscheinlichkeit, dass Ihr Vater - ebenso wie
Sie - offen gegenüber Veränderungen und Neuheiten in seiner Umgebung
ist.
43
Forschungsergebnisse belegen, dass Aufgeschlossenheit zu 61% erblich
bedingt wird. Der Grad der Aufgeschlossenheit von Kindern ist
meistens dem des Vaters ähnlich.
Harmoniebedürfnis
Es ist gut möglich, dass Sie in Ihrem Harmoniebedürfnis weder Ihrem
Vater noch Ihrer Mutter ähnlich sind.
Zahlreiche Studien beschäftigen sich mit der Vererbbarkeit des
Harmoniebedürfnisses - bis heute ist die Wissenschaft aber noch zu
keinem eindeutigen Ergebnis gekommen. Derzeit geht man davon
aus, dass der Grad des Harmoniebedürfnisses vielmehr von Erfahrungen
und Erlebnissen bestimmt wird als von vererbten Faktoren.
In diesem Abschnitt haben Sie gelernt, dass unsere Persönlichkeit
stark von unseren instinktgesteuerten und genetischen Veranlagungen
bestimmt wird. Doch auch unsere Umgebung und die Erfahrungen,
die wir im Verlauf unseres Lebens machen, haben einen starken Einfluss
auf uns. Dies ist das Thema des nächsten Abschnitts.</p>


