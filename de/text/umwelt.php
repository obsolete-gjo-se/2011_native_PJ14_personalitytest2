als die Ähnlichkeiten zwischen den Geschwistern betonen. Diese
Unterscheidung, auch "Geschwister-Effekt" genannt, hat einen guten
Grund: Jedes Kind soll eine Chance haben, Aufmerksamkeit für seine
einzigartigen Fähigkeiten zu bekommen. Der Effekt ist besonders
stark ausgeprägt bei Kindern desselben Geschlechts und bei jenen,
deren Altersunterschied weniger als vier Jahre beträgt.
Sie können den Geschwister-Effekt selbst testen, indem Sie das
nächste Mal, wenn Sie Eltern treffen, diese bitten, ihre Sprösslinge zu
beschreiben. Anstatt zu sagen "Ich habe zwei Söhne, die beide aktiv
und intelligent sind und gerne Sport treiben", wird die Person höchstwahrscheinlich
die Unterschiede zwischen den Kindern betonen:
"Sven, mein Ältester, liebt Fußball und ist ein begabter Zeichner. Tim,
der Kleine, spielt gerne Basketball und hat eine Eins in Mathe."
Ihre Persönlichkeit wurde von den Leuten geformt, mit denen Sie
aufwuchsen.
Die Bedeutung der Geburtenfolge
Welche anderen Faktoren haben Ihre Persönlichkeit wesentlich geformt?
Psychologen haben entdeckt, dass zusätzlich zum Geschwister-
Effekt einer der wichtigsten Einflüsse auf die Persönlichkeitsentwicklung
Ihre Geburtenfolge, d.h. Ihre Position in der Familie, darstellt.
Durch Ihren Platz in der Familie (ältestes, mittleres, jüngstes oder
Einzelkind) entwickeln Sie gewisse Gewohnheiten und gefühlsmäßige
Reaktionen, die sehr nachhaltig sein können. Abgesehen von den Erwartungen
an Sie als ,Sohn' oder ,Tochter', ,Kind' oder ,Schüler', verschafft
Ihnen die Position, in der Sie in Ihre Familie hineingeboren
werden, Ihre erste Rolle im Leben. Der Effekt Ihrer Position in der
Familie ist außerordentlich stark; auf ihr basieren Ihre Strategien für
den Umgang mit anderen, die Sie bis ins Erwachsenenleben hinein
begleiten.
