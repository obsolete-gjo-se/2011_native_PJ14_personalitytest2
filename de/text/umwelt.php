Anhand Ihrer Persönlichkeits-Auswertung lässt sich der Einfluss
Ihrer Position als jüngstes Kind auf Ihre Persönlichkeit erkennen. Sie
sind sensibel für die Stimmungen anderer und können rasch feststellen,
ob diese sich niedergeschlagen oder ausgeschlossen fühlen. Sie haben
das Gefühl, es liege in Ihrer Verantwortung, andere aufzumuntern.
Das kann manchmal eine schwere Bürde sein, und deshalb fühlen Sie
sich leicht von Menschen gestört, die zu abhängig oder anhänglich
sind. Sie hegen die unterschwellige Angst, dass andere Sie nicht ernst
genug nehmen, und arbeiten hart, um sich zu beweisen. Es ist nicht
unwahrscheinlich, dass diese Gewohnheiten von Ihrer Rolle als
jüngstem Kind oder ,Baby' der Familie beeinflusst sind.
Ihre soziale Seite: Studien über jüngste Kinder ergeben, dass sie
sich hinsichtlich Freunden oder Partnern wahrscheinlich am ehesten
zu Erstgeborenen oder Einzelkindern hingezogen fühlen. Sie sind von
selbstständigen Menschen beeindruckt.
Wenn sich Ihnen die Gelegenheit bietet, machen Sie eine Umfrage
über die Geburtenfolge Ihrer Freunde und Kollegen. Sollte bei der Art
von Mensch, mit der Sie am besten zurechtkommen, ein bestimmtes
Muster auftauchen, achten Sie darauf, welche persönlichen Bedürfnisse
Ihrerseits dies widerspiegeln könnte.</p>

